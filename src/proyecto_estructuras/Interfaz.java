    /**
     * Método Leertexto
     * Carga el grafo desde un archivo .txt.
     * Lee el archivo línea por línea, buscando los títulos "Usuarios"
     * y "Relaciones" para saber si debe crear Nodos o Aristas.
     *
     * busca "Usuarios" y "Relaciones" 
     *
     * @param archivo El .txt para cargar.
     * @param grafito El grafo (vacío) que se va a llenar.
     */
    public void Leertexto(File archivo, Grafo grafito) {
        this.Respaldo_V3 = archivo.getAbsolutePath();
        try (BufferedReader buffer = new BufferedReader(new FileReader(archivo))) {
            String aux;
            boolean leerUsuarios = false;
            boolean leerRelaciones = false;

            while ((aux = buffer.readLine()) != null) {
                aux = aux.trim();
                if (aux.isEmpty()) {
                    continue;
                }

                if (aux.equalsIgnoreCase("Usuarios")) {
                    leerUsuarios = true;
                    leerRelaciones = false;
                    continue;
                }
                if (aux.equalsIgnoreCase("Relaciones")) {
                    leerUsuarios = false;
                    leerRelaciones = true;
                    continue;
                }

                if (leerUsuarios) {
                    grafito.NuevoNodo(aux);
                    continue;
                }

                if (leerRelaciones) {
                    String[] partes = aux.split(",");
                    if (partes.length == 2) {
                        String origen = partes[0].trim();
                        String destino = partes[1].trim();
                        grafito.NuevaArista(origen, destino);
                        this.Archivo_txt_Cargado = true;
                    }
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,
                    "Error al leer el archivo: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            this.Archivo_txt_Cargado = false;
        }

    }

